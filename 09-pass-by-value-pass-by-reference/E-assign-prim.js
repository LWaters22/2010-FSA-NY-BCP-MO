// ------------------------------------------------
// PART 2 OF PRIMITIVE ASSIGNMENT - ANOTHER EXAMPLE
// ------------------------------------------------

let first = 20; // first stores the value 20
let second = first; // second stores its own copy of the value 20
/*
first: 20
second: 20
*/

second++; // has no effect on the value stored in first!
/*
first: 20
second: 21
*/

console.log(first);
console.log(second);

